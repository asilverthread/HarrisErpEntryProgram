    private void btnAddFeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFeeActionPerformed
        //If there are already transactions listed, prompt the user to clear the display before proceeding with adding more fees and payments
        if (lstTransactions.getModel().getSize()!=0){
            int selection = JOptionPane.showConfirmDialog(this,"Clear listed payments, fees, and transactions to add more?","Clear screen to proceed?",JOptionPane.YES_NO_OPTION);
            if (selection == JOptionPane.YES_OPTION){
                resetLists();
            }else{
                return;
            }
        }
        try{
            float fa = Float.parseFloat(txtFeeAmount.getText());
            String fn = txtFeeName.getText();
            ph.addFee(new Fee(fa,fn));          
        }catch(Exception e){
            //System.err.println(e);
            JOptionPane.showMessageDialog(this, "User input could not be parsed to decimal format.\nCheck input and try again!");
        }
        lstFees.setModel(buildFees(ph.getFees()));
    }//GEN-LAST:event_btnAddFeeActionPerformed

    private void btnAddPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPayActionPerformed
        //If there are already transactions listed, prompt the user to clear the display before proceeding with adding more fees and payments
        if (lstTransactions.getModel().getSize()!=0){
            int selection = JOptionPane.showConfirmDialog(this,"Clear listed payments, fees, and transactions to add more?","Clear screen to proceed?",JOptionPane.YES_NO_OPTION);
            if (selection == JOptionPane.YES_OPTION){
                resetLists();
            }else{
                return;
            }
        }
        try{
            float p = Float.parseFloat(txtPayment.getText());
            ph.addPayment(new Payment(p));
        }catch(Exception e){
            //System.err.println(e);
            JOptionPane.showMessageDialog(this, "User input could not be parsed to decimal format.\nCheck input and try again!");
        }
        lstPayments.setModel(buildPayments(ph.getPayments()));        
    }//GEN-LAST:event_btnAddPayActionPerformed

    private void btnApplyPaymentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyPaymentsActionPerformed
        if (lstTransactions.getModel().getSize()!=0){
            JOptionPane.showMessageDialog(this,"Transactions have already been built for the given lists");
            return;  
        }
        ph.applyPayments();
        lstTransactions.setModel(buildTransactions(ph.getTransactions()));
    }//GEN-LAST:event_btnApplyPaymentsActionPerformed

