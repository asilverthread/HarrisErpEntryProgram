    private void resetLists(){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        lstFees.setModel(dlm);
        lstTransactions.setModel(dlm);
        lstPayments.setModel(dlm);
        ph.resetLists();
    }
    private static DefaultListModel buildPayments(ArrayList<Payment> arr){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        arr.forEach((n)->{dlm.addElement(Float.toString(n.getAmount()));});
        return dlm;
    }
    private static DefaultListModel buildFees(ArrayList<Fee> arr){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        arr.forEach((n)->{dlm.addElement(n.getName()+": "+Float.toString(n.getAmount()));});
        return dlm;
    }
    private static DefaultListModel buildTransactions(ArrayList<Transaction> arr){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        arr.forEach((n)->{dlm.addElement(Float.toString(n.getAmount())+" for "+n.getFee().getName());});
        return dlm;
    }
